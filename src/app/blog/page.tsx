const Blog = () => {
  const [searchTerm, setSearchTerm] = useState("");
  const [filter, setFilter] = useState("all");

  const filteredArticles = articles.filter((article: Article) => {
    const matchesSearch =
      article.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
      article.excerpt.toLowerCase().includes(searchTerm.toLowerCase()) ||
      article.body.toLowerCase().includes(searchTerm.toLowerCase());

    const matchesFilter =
      filter === "all" ||
      (filter === "sugar" && article.title.includes("سكر")) ||
      (filter === "digestion" &&
        (article.title.includes("قولون") || article.title.includes("معدة"))) ||
      (filter === "heat" &&
        (article.title.includes("حر") || article.title.includes("شمس")));

    return matchesSearch && matchesFilter;
  });

  const resetFilters = () => {
    setSearchTerm("");
    setFilter("all");
  };

  return (
    <>
      <div className="fixed z-100 border-b-[0.1px] bg-white border-[#009a8bbe]">
        <Header />
      </div>

      <div className="w-full  sm:h-[125vh] md:h-[115vh] bg-[#F2FDFB] pt-[160px] flex flex-col items-center">
        {/* Search & Filter Controls */}
        <div className="flex flex-col md:flex-row gap-3 mb-0 w-[90%] max-w-4xl ">
          <input
            type="text"
            placeholder="ابحث عن مقال..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="flex-1 px-5 py-3 border rounded-2xl shadow-sm bg-white text-gray-700
               placeholder-gray-400 focus:outline-none focus:ring-2 
               focus:ring-[var(--color-primary)] focus:border-[var(--color-primary)]
               transition duration-200"
          />

          <select
            value={filter}
            onChange={(e) => setFilter(e.target.value)}
            className="px-5 py-3 border rounded-2xl shadow-sm bg-white text-gray-700
               focus:outline-none focus:ring-2 focus:ring-[var(--color-primary)]
               focus:border-[var(--color-primary)] transition duration-200"
          >
            <option value="all" className="">
              كل المقالات
            </option>
            <option value="sugar">أمراض السكر</option>
            <option value="digestion">الجهاز الهضمي</option>
            <option value="heat">مشاكل الحرارة</option>
          </select>

          {searchTerm || filter !== "all" ? (
            <button
              onClick={resetFilters}
              className="px-5 py-3 rounded-2xl shadow-sm border bg-white text-red-500 
               border-red-400 hover:bg-red-500 hover:text-white
               transition duration-200 font-medium"
            >
              إعادة التعيين
            </button>
          ) : null}
        </div>

        {/* Blog Section */}
        <BlogSection articles={filteredArticles} />
      </div>

      <Footer />
    </>
  );
};

export default Blog;
